# Problem Statement: Convert temperature from Celsius to Fahrenheit.

# taking input from the user
celsius = float(input("Enter temperature in Celsius: "))

# calculating
fahrenheit = (celsius * 9/5) + 32

# printing
print("Temperature in Fahrenheit:", fahrenheit)

# //output--------
# Enter temperature in Celsius: 342
# Temperature in Fahrenheit: 647.6 ----------------//